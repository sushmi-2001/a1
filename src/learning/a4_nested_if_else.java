package learning;

public class a4_nested_if_else {

	public static void main(String[] args) {

		int a = 10, b = 20, c = 5;
		if (a > b) {
			if (a > c) {
				System.out.println(a);

			} else {
				System.out.println(a);

			}

		} else {
			if (b > c) {
				System.out.println(b);
			} else {
				System.out.println(c);
			}

		}
	}

}
